package p5;

/**
 * Represents the size of a pizza.
 * Each size corresponds to a different portion and price for the pizza.
 * @author Siddharth, Ibtesaam
 */
public enum Size {

    /**
     * Small size for the pizza.
     * Typically, serves 1-2 people.
     */
    SMALL,

    /**
     * Medium size for the pizza.
     * Typically, serves 2-3 people.
     */
    MEDIUM,

    /**
     * Large size for the pizza.
     * Typically, serves 3-4 people.
     */
    LARGE
}

